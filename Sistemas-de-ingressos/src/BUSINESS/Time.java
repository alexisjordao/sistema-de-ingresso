public class Time {
    private String date = null;
    private String hour = null;
    
   public String getDate()
   {
       return this.date;
   }
   
   public void setDate(String date)
   {
       this.date = date;
   }
   
   public String getHour()
   {
       return this.hour;
   }
   
   public void setHour(String hour)
   {
       this.hour = hour;
   } 
    
}
